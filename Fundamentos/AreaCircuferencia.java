public class AreaCircuferencia {
    public static void main(String[] args) {
        /**
         * @autor Vanderson Batista
         */
        double raio = 3.4;
        final double PI = 3.14159; // pi não é varial ela é constante, com o comando final antes.
        // Colocar o nome da constante em maiusculo.
        double area = PI * raio * raio;

        System.out.println("Área = " + area);

    }
}
