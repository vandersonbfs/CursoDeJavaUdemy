public class Aritimeticos {
    public static void main(String[] args) {
        System.out.println("Aula 42 Operadores aritimeticos");
        var x = 34.56;
        double y = 2.2;

        System.out.println(" ");
        System.out.println("Soma .................: " + (x + y));
        System.out.println("Subtração ............: " + (x - y));
        System.out.println("Multiplicação ........: " + (x * y));
        System.out.println("Divisão ..............: " + (x / y));

        int a = 8;
        int b = 3;
        System.out.println(" ");
        System.out.println("Operadores com valores inteiros ");
        System.out.println("Soma .................: " + (a + b));
        System.out.println("Subtração ............: " + (a - b));
        System.out.println("Multiplicação ........: " + (a * b));
        System.out.println("Divisão ..............: " + (a / b));

        System.out.println(" ");
        System.out.println("Resto da divisão de " + a + " / " + b);
        System.out.println("Resto ...............: " + (a - b));

    }
}
